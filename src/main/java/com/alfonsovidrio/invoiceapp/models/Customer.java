package com.alfonsovidrio.invoiceapp.models;

public class Customer {
    private String name;
    private String rfc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRfc() {
        return rfc;
    }

    public void setRfc(String rfc) {
        this.rfc = rfc;
    }
}
